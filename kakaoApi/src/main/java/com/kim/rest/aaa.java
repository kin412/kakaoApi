package com.kim.rest;

public class aaa {
	private String no;
	private String name;
	private String nick;
	
	public String getNo() {
		return no;
	}
	public void setNo(String no) {
		this.no = no;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getNick() {
		return nick;
	}
	public void setNick(String nick) {
		this.nick = nick;
	}
	
	@Override
	public String toString() {
		return "aaa [no=" + no + ", name=" + name + ", nick=" + nick + "]";
	}
	
	
	
	
	
}
